    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jLabel2MouseClicked
        // TODO add your handling code here:
        int ID = 0;
        try{
            ID = Integer.parseInt(dustid.getText());
        }
        catch (NumberFormatException ex){
            System.out.println(ex);
        }

        int flag = 0;
        for (int i = 0; i < dustbin_management.Dustbin_Management.dustbinlist.size(); i++) {

            if (dustbin_management.Dustbin_Management.dustbinlist.get(i).id == ID) {
                flag = 1;
                k = i;
            }
        }
        if (flag == 0) {
            k=0;
            print.setText("");
            JOptionPane.showMessageDialog(null, "Invaid Dustbin Id", "Dustbin not registered",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            // SHOW DETAILS...
            String LOCATION = dustbin_management.Dustbin_Management.dustbinlist.get(k).location;
            double WIDTH = dustbin_management.Dustbin_Management.dustbinlist.get(k).width;
            double HEIGHT = dustbin_management.Dustbin_Management.dustbinlist.get(k).height;
            int LEVEL = dustbin_management.Dustbin_Management.dustbinlist.get(k).level;
            String LASTEMPTYDATE = dustbin_management.Dustbin_Management.dustbinlist.get(k).lastEmptyDate;

            print.setText("Location: " + LOCATION + "\n\n" + "Width: " + WIDTH + "\n\n" + "Height: " + HEIGHT + "\n\n"
                    + "Level: " + LEVEL + "\n\n" + "Last Empty Date: " + LASTEMPTYDATE);

        }

    }// GEN-LAST:event_jLabel2MouseClicked

