    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(k == 0)
            return;
            
        try{
            String URL = "jdbc:mysql://localhost:3306/smart_dustbin";
            String USER_NAME = "manish";
            String PASSWORD = "manish";
            String query="UPDATE dustbin SET Level = 0 where Dustbin_ID = ?";
            
            Connection conn = DriverManager.getConnection(URL, USER_NAME, PASSWORD);;
            PreparedStatement stmt = conn.prepareStatement(query);;
            stmt.setString(1, Integer.toString(dustbin_management.Dustbin_Management.dustbinlist.get(k).id));
            stmt.executeUpdate();

            dustbin_management.Dustbin_Management.dustbinlist.get(k).level = 0;
            JOptionPane.showMessageDialog(null, "Dustbin Cleared successfully", "Cleared", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception e)
        {
             JOptionPane.showMessageDialog(null, e);
        }
        
    }// GEN-LAST:event_jButton2ActionPerformed

